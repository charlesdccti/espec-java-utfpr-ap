    private Object[][] getRows(List<Produto> itens) {
	Object[][] itensTab = new Object[itens.size()][2];
	int row = 0;
	for(Produto prod : itens) {
	    Object[][] line = {prod.row()};
	    System.arraycopy(line, 0, itensTab, row, 1);
	    row++;
	}
	return itensTab;
    }
    
    private void habilita(boolean estado) {
	btnIncluir.setEnabled(estado);
	btnExcluir.setEnabled(!estado);
	abasProduto.setEnabledAt(0, estado);
	abasProduto.setEnabledAt(1, !estado);
	if (estado) {
	    List<Produto> produtos = controlProd.listaDeProdutos();
	    tbProduto.setModel(new BotiqueTableModel(getRows(produtos), tbProdutoTitle));
	    abasProduto.setSelectedIndex(0);
	} else {
	    abasProduto.setSelectedIndex(1);
	    txDescricao.requestFocus();
	}
    }

