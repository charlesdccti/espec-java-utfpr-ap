    private void tbProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProdutoMouseClicked
	if (evt.getClickCount() == 2) {
	    JTable table = (JTable) evt.getSource();
	    int id = Integer.parseInt(table.getValueAt(table.getSelectedRow(), 0).toString());
	    System.out.println("O valor do id dessa linha é: " + id);
	    produto = controlProd.getProduto(id);
	    txId.setText(String.valueOf(produto.getId()));
	    txDescricao.setText(produto.getDescricao());
	    habilita(false);
	    acao = AcaoDoControle.EDITAR;
	}
    }//GEN-LAST:event_tbProdutoMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
	if(!txPesquisa.getText().equals("")) {
	    List<Produto> produtos = controlProd.listaDeProdutosPorDescricao(txPesquisa.getText());
	    tbProduto.setModel(new BotiqueTableModel(getRows(produtos), tbProdutoTitle));
	}
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
	if (acao == AcaoDoControle.INSERIR) {
	    produto = new Produto(txDescricao.getText());
	    controlProd.inserir(produto);
	} else if (acao == AcaoDoControle.EDITAR) {
	    produto = new Produto(txDescricao.getText());
	    produto.setId(Integer.parseInt(txId.getText()));
	    controlProd.atualizar(produto);
	}
	habilita(true);
    }//GEN-LAST:event_btnConfirmarActionPerformed

